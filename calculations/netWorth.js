

export default function calculateNetWorth(retirementAge, ageOfDeath) {

    function calculateNetWorthByAge(i) {
        if (i => 0) {
        return Math.floor(5);
        }
    }

    const data = {}
    for (let i = retirementAge; i <= ageOfDeath; i++) {
        data[i] = calculateNetWorthByAge(i);
    };
    return data;
}