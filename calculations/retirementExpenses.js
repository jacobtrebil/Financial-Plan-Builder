

export default function calculateRetirementExpenses(retirementAge, ageOfDeath, livingExpense) {
    
    function calculateRetirementExpensesByAge(i, livingExpense) {
        if (i => 0) {
        return Math.floor(livingExpense);
        }
    }
    
    const data = {}
    for (let i = retirementAge; i <= ageOfDeath; i++) {
        data[i] = calculateRetirementExpensesByAge(i, livingExpense);
    };
    return data;
}